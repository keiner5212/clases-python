# Solicitamos al usuario que ingrese un valor utilizando la funci√≥n input
x = input("Ingresa \"verde\": ")

# Utilizamos una estructura condicional if-else para verificar si el valor ingresado es igual a "verde"
if x == "verde":
    # Si el valor ingresado es igual a "verde", mostramos el mensaje "cierto"
    print("cierto")
else:
    # Si el valor ingresado no es igual a "verde", mostramos el mensaje "falso"
    print("falso")